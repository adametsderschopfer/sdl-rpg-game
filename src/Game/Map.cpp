Map::Map() {
    m_dirt = TextureManager::loadTexture("assets/dirt.jpg");
    m_grass = TextureManager::loadTexture("assets/grass.jpg");
    m_water = TextureManager::loadTexture("assets/water.png");

    loadMap(lvl1);

    m_destRect.h = 32;// todo remove magic number
    m_destRect.w = 32;// todo remove magic number

    m_destRect.x = m_destRect.y = 0;
}

Map::~Map() {
    SDL_DestroyTexture(m_grass);
    SDL_DestroyTexture(m_water);
    SDL_DestroyTexture(m_dirt);
}

void Map::loadMap(int arr[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 25; column++) {
            m_map[row][column] = arr[row][column];
        }
    }
}

void Map::drawMap() {
    int type = 0;

    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 25; column++) {
            type = m_map[row][column];

            m_destRect.x = static_cast<float >(column * 32);// todo remove magic number
            m_destRect.y = static_cast<float >(row * 32) ;// todo remove magic number

            switch (type) {
                case 0:
                    TextureManager::draw(m_water, m_destRect);
                    break;
                case 1:
                    TextureManager::draw(m_grass, m_destRect);
                    break;
                case 2:
                    TextureManager::draw(m_dirt, m_destRect);
                    break;
                default:
                    break;
            }
        }
    }
}

